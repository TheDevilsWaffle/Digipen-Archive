    ///////////////////////////////////////////////////////////////////////////////////////////////
    void UpdateShieldIntensity(EVENT_UPDATE_SHIELD_ENERGY _event)
    {
        //Debug.Log("UpdateShieldIntensity(" + _event.energy + ")");
        shieldMaterial.color = new Color(0f, (_event.energy / intensityFactor), ((_event.energy / intensityFactor) * 0.4f), (_event.energy / intensityFactor));
    }

    #endregion

    #region ONDESTORY
    ///////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// OnDestroy
    /// </summary>
    ///////////////////////////////////////////////////////////////////////////////////////////////
    void OnDestroy()
    {
        //remove listeners
        Events.instance.RemoveListener<EVENT_UPDATE_SHIELD_ENERGY>(UpdateShieldIntensity);
    }
