#region EVENTS
public class EVENT_LOAD_CODA : GameEvent
{
    public Canvas canvasToLoad;
    public MenuToLoad menuToLoad;
    public EVENT_LOAD_CODA(Canvas _canvasToLoad, MenuToLoad _menuToLoad)
    {
        canvasToLoad = _canvasToLoad;
        menuToLoad = _menuToLoad;
    }
}
#endregion

public class LoadCODA : ButtonActionBase
{
    #region FIELDS
    [SerializeField]
    MenuToLoad menuToLoad;
    [SerializeField]
    Canvas canvasToLoad;
    #endregion

    #region INITIALIZATION
    ///////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Awake()
    /// </summary>
    ///////////////////////////////////////////////////////////////////////////////////////////////
    void Awake()
    {
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Start()
    /// </summary>
    ///////////////////////////////////////////////////////////////////////////////////////////////
    void Start()
    {
    }
    #endregion

    #region PUBLIC METHODS
    ///////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// 
    /// </summary>
    ///////////////////////////////////////////////////////////////////////////////////////////////
    public override void Activate()
    {
        Debug.Log("message sent");
        base.Activate();
        Events.instance.Raise(new EVENT_LOAD_CODA(canvasToLoad, menuToLoad));
    }
    #endregion

    #region PRIVATE METHODS
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////////////
    #endregion

    #region ONDESTORY
    ///////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// OnDestroy()
    /// </summary>
    ///////////////////////////////////////////////////////////////////////////////////////////////
    void OnDestroy()
    {
        //remove listeners
    }
    #endregion
}
