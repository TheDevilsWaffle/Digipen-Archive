using UnityEngine;
using System.Collections;
//using System.Collections.Generic;
//using UnityEngine.UI;

#region ENUMS

#endregion

#region EVENTS

#endregion

public class Billboard : MonoBehaviour
{
    #region FIELDS
    public GameObject target;
    public bool overridePosition;
    public Vector3 pos;
    public bool overrideScale;
    public Vector3 sca;
    Transform target_tr;
    Transform tr;
    #endregion

    #region INITIALIZATION
    ///////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Awake()
    /// </summary>
    ///////////////////////////////////////////////////////////////////////////////////////////////
    void Awake()
    {
        tr = transform;
        target_tr = target.transform;
    }

	///////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Start()
    /// </summary>
    ///////////////////////////////////////////////////////////////////////////////////////////////
    void Start()
    {
        if (overrideScale)
        {
            tr.localScale = sca;
        }
	}
    #endregion

    #region UPDATE
    ///////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Update()
    /// </summary>
    ///////////////////////////////////////////////////////////////////////////////////////////////
    void Update()
    {
        tr.position = target_tr.position;
        transform.LookAt(tr.position + target_tr.rotation * Vector3.up,
                         target_tr.rotation * Vector3.up);
        if(overridePosition)
        {
            tr.position += pos;
        }
    }
    #endregion

    #region METHODS
    ///////////////////////////////////////////////////////////////////////////////////////////////
    
    ///////////////////////////////////////////////////////////////////////////////////////////////
    #endregion
}
