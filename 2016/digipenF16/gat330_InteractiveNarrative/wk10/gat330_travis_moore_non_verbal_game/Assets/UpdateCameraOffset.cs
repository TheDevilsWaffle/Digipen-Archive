using UnityEngine;
using System.Collections;
//using System.Collections.Generic;
//using UnityEngine.UI;

#region ENUMS

#endregion

#region EVENTS

#endregion

public class UpdateCameraOffset : MonoBehaviour
{
    #region FIELDS
    Camera camera;
    #endregion

    #region INITIALIZATION
    ///////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Awake()
    /// </summary>
    ///////////////////////////////////////////////////////////////////////////////////////////////
    void Awake()
    {
        camera = GetComponent<Camera>();
        Events.instance.AddListener<EVENT_FollowerDied>(UpdateOffset);
    }

	///////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Start()
    /// </summary>
    ///////////////////////////////////////////////////////////////////////////////////////////////
    void Start()
    {
	
	}
    #endregion

    #region METHODS
    void UpdateOffset(EVENT_FollowerDied _event)
    {
        camera.fieldOfView = camera.fieldOfView - 2;
    }

    void OnDestroy()
    {
        Events.instance.RemoveListener<EVENT_FollowerDied>(UpdateOffset);
    }
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////////////
    #endregion
}
