using UnityEngine;
using System.Collections;
//using System.Collections.Generic;
//using UnityEngine.UI;

#region ENUMS

#endregion

#region EVENTS

#endregion

public class SFXSystem : MonoBehaviour
{
    #region FIELDS
    public static SFXSystem sfxSystem;
    [SerializeField]
    AudioSource sfx;
    #endregion

    #region INITIALIZATION
    ///////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Awake()
    /// </summary>
    ///////////////////////////////////////////////////////////////////////////////////////////////
    void Awake()
    {
        if (sfxSystem == null)
        {
            sfxSystem = this;
        }
        else if (sfxSystem != this)
        {
            Destroy(gameObject);
        }

    }

	///////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Start()
    /// </summary>
    ///////////////////////////////////////////////////////////////////////////////////////////////
    void Start()
    {
	
	}
    #endregion

    #region METHODS
    public void PlaySFX(AudioClip _sfx)
    {
        sfx.PlayOneShot(_sfx);
    }
    ///////////////////////////////////////////////////////////////////////////////////////////////
    
    ///////////////////////////////////////////////////////////////////////////////////////////////
    #endregion
}
